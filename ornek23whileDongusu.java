public static void main(String[] args) {
        
       int sayi = 5;
       //doğru sınama değişkeni
       while(sayi >= 0){
           //dongü koşulu
           System.out.println("5 kere dondureceğiz");
           sayi--;
       }
       // 1 den 10 kadar sayilari ekrana bastıralım
       int abakus = 0;
       while(abakus<=10){
           System.out.println("abakus =" + abakus);
           abakus ++;
       }
       System.out.println("abakus doğru sayiyor:)");
    }
