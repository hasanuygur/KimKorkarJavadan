public static void main(String[] args) {
        
       int x = 7 , y = 5;
       
       System.out.println(x<y);//x,y den küçük mü
       
       System.out.println(x + "<" + y + ":" + (x<y));
       //yukarıdaki her iki ifadede matematiksel işlemi döndürür
       //ancak ikincisi daha anlaşılır olmuştur.
       
       System.out.println();//boşluk bırakalım
       System.out.println(x + ">" + y + ":" + (x>y));//x yden büyük mü
       System.out.println(x + "<=" + y + ":" + (x<=y));//x yden küçük eşitmi
       System.out.println(x + ">=" + y + ":" + (x>=y));//x yden büyük eşitmi
       System.out.println(x + "==" + y + ":" + (x==y));//x yeye eşitmi
       System.out.println(x + "!=" + y + ":" + (x!=y));//x yeye eşit değilmi
       
       
    }
